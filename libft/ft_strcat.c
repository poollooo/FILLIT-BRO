#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *s1c;

	s1c = s1;
	while (*s1c != '\0')
		s1c++;
	while (*s2 != '\0')
		*s1c++ = *s2++;
	*s1c = '\0';
	return (s1);
}
